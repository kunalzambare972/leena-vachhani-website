// Filter publications based on type
function filterPublications(type) {
  var items = document.querySelectorAll(".publication-item");

  items.forEach(function (item) {
    // If 'All' is selected, show all items
    if (type === "all") {
      item.style.display = "block";
    }
    // Show only items matching the selected type
    else if (item.getAttribute("data-type") === type) {
      item.style.display = "block";
    }
    // Hide other items
    else {
      item.style.display = "none";
    }
  });
}
